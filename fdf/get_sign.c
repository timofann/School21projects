int	get_sign(char **arg)
{
	int	sign;

	sign = 1;
	if (**arg == '-')
	{
		sign = -1;
		(*arg)++;
	}
	while (**arg == '+')
	{
		sign = 1;
		(*arg)++;
		if (**arg == '-')
		{
			sign = -1;
			(*arg)++;
		}
		else
			break ;
	}
	return (sign);
}
